-- Create sales table
CREATE TABLE sales (
    sale_id NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY PRIMARY KEY,
    order_date DATE,
    customer_id NUMBER,
    product_id NUMBER,
    quantity NUMBER,
    sale_date DATE,
    CONSTRAINT fk_customer FOREIGN KEY (customer_id) REFERENCES customers(customer_id),
    CONSTRAINT fk_product FOREIGN KEY (product_id) REFERENCES inventories(product_id)
